TIME=$FFFF


START:
	MOVE.L	4.W,A6
	JSR	-$84(A6)	; Forbid
	JSR	-$78(A6)	; Disable

	LEA	$BFD000,A4
	MOVE.B	$E00(A4),D0
	ANDI.B	#%11000000,D0
	ORI.B	#%00001000,D0
	MOVE.B	D0,$E00(A4)
	MOVE.B	#%01111110,$D00(A4)
	MOVE.B	#(TIME&$FF),$400(A4)
	MOVE.B	#(TIME>>8),$500(A4)
	BSET.B	#0,$E00(A4)

.WAIT
	BTST.B	#0,$D00(A4)
	BEQ.B	.WAIT

	MOVE.L	4.W,A6
	JSR	-$7E(A6)	; Enable
	JSR	-$8A(A6)	; Permit

	RTS
